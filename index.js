function filterString(string, searchTerm) {
  if (
    typeof string === 'string' &&
    string
      .replace(/-|\s/g, '')
      .toLowerCase()
      .includes(searchTerm.toLowerCase())
  ) {
    return true;
  }
  return false;
}

function filterArray(array, searchTerm) {
  if (
    Array.isArray(array) === true &&
    array[0] !== undefined &&
    array[0] !== null &&
    typeof array[0] != 'number' &&
    array[0]
      .toString()
      .replace(/-|\s/g, '')
      .toLowerCase()
      .includes(searchTerm.toLowerCase())
  ) {
    return true;
  }
  return false;
}

function search(searchTerm) {
  return photographs.filter(photograph => {
    return (
      Object.values(photograph).filter(value => {
        if (filterString(value, searchTerm) === true) {
          return true;
        } else if (filterArray(value, searchTerm) === true) {
          return true;
        }
        return false;
      }).length > 0
    );
  });
  return searchArray;
}

